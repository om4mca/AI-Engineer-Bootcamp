numbers = [12, 10, 22, 33, 7, 19, 24, 8, 5, 10,50]

# Keep 'num' only if dividing it by 2 leaves a remainder
odd_numbers = [num for num in numbers if num % 2 != 0]

print(odd_numbers)  
