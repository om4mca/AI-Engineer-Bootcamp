doctors = ("Dr. Om", "Dr. Shyam", "Dr. Ramesh", "Dr. Kumar")

# Positive Indexing
print(doctors[0])   # Output: Dr. Om (First item)
print(doctors[2])   # Output: Dr. Ramesh    (Third item)

# Negative Indexing
print(doctors[-1])  # Output: Dr. Kumar  (Last item)
print(doctors[-2])  # Output: Dr. Ramesh    (Second to last item)

